export function createSong(data) {
  const hasAutoDetected = (data.chords || []).some(c => c.autoDetected)
  return {
    id: data.id || Date.now().toString(36) + Math.random().toString(36).substr(2, 5),
    title: data.title || 'Untitled Song',
    artist: data.artist || 'Unknown',
    chords: data.chords || [],
    bpm: data.bpm || 120,
    totalTime: data.totalTime || 0,
    source: data.source || 'manual',
    sourceUrl: data.sourceUrl || '',
    embedUrl: data.embedUrl || null,
    thumbnailUrl: data.thumbnailUrl || null,
    lyrics: data.lyrics || [],
    sections: data.sections || [],
    sectionsWithLyrics: data.sectionsWithLyrics || [],
    timing: data.timing || [],
    notes: data.notes || [],
    leadNotes: data.leadNotes || [],
    parts: data.parts || [],
    backgroundMusicUrl: data.backgroundMusicUrl || null,
    tempo: data.tempo || { playbackSpeed: 1, pitchCorrection: false },
    loopSettings: data.loopSettings || { enabled: false, startBeat: 0, endBeat: 0, count: Infinity },
    chordsAutoDetected: data.chordsAutoDetected !== undefined ? data.chordsAutoDetected : hasAutoDetected,
    parsedMetadata: data.parsedMetadata || {
      platform: data.source || 'manual',
      platformId: null,
      parsedAt: new Date().toISOString(),
      format: 'manual',
      autoDetected: false
    },
    progress: { completedBeats: 0, mastered: false },
    addedAt: new Date().toISOString()
  }
}